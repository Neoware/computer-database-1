package com.excilys.core.dto;

import java.io.Serializable;
import java.time.LocalDate;

import javax.validation.constraints.Pattern;
import javax.validation.constraints.Size;

import com.excilys.core.date.DateMapper;
import com.excilys.core.entity.Company;
import com.excilys.core.validator.DateValidator;

/**
 * Representation of a Company object which is given to the front.
 * @author pqwarlot
 *
 */
public class ComputerDTO implements Serializable {
	@Size(max=20)
	private String id;
	@Size(min=1, max=255)
	private String name;
	@Size(max=10)
	@Pattern(regexp=DateValidator.DATE_REGEX)
	private String introduced;
	@Size(max=10)
	@Pattern(regexp=DateValidator.DATE_REGEX)
	private String discontinued;
	@Size(max=20)
	private String companyId;
	private String companyName;

	public ComputerDTO() {
	}
	
	/**
	 * Create a ComputerDTO object, set computer id and computer company to empty string.
	 * @param name
	 * @param introduced
	 * @param discontinued
	 * @param companyId
	 */
	public ComputerDTO(String name, String introduced, String discontinued, String companyId) {
		this("", name, introduced, discontinued, companyId, "");
	}
	
	public ComputerDTO(Long id, String name, LocalDate introduced, LocalDate discontinued, Company company) {
		this((id == null) ? "" : String.valueOf(id),
			name,
			DateMapper.convertLocalDateToString(introduced),
			DateMapper.convertLocalDateToString(discontinued),
			(company == null || company.getId() == null) ? "" : company.getId().toString(),
			(company == null) ? "" : company.getName());
		
	}
	
	public ComputerDTO(String id, String name, String introduced, String discontinued, String companyId,
			String companyName) {
		this.id = id;
		this.name = name;
		this.introduced = introduced;
		this.discontinued = discontinued;
		this.companyId = companyId;
		this.companyName = companyName;
	}
	
	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getIntroduced() {
		return introduced;
	}

	public void setIntroduced(String introduced) {
		this.introduced = introduced;
	}

	public String getDiscontinued() {
		return discontinued;
	}

	public void setDiscontinued(String discontinued) {
		this.discontinued = discontinued;
	}

	public String getCompanyId() {
		return companyId;
	}

	public void setCompanyId(String companyId) {
		this.companyId = companyId;
	}

	public String getCompanyName() {
		return companyName;
	}

	public void setCompanyName(String companyName) {
		this.companyName = companyName;
	}

	@Override
	public int hashCode() {
		final int prime = 31;
		int result = 1;
		result = prime * result + ((companyId == null) ? 0 : companyId.hashCode());
		result = prime * result + ((companyName == null) ? 0 : companyName.hashCode());
		result = prime * result + ((discontinued == null) ? 0 : discontinued.hashCode());
		result = prime * result + ((id == null) ? 0 : id.hashCode());
		result = prime * result + ((introduced == null) ? 0 : introduced.hashCode());
		result = prime * result + ((name == null) ? 0 : name.hashCode());
		return result;
	}

	@Override
	public boolean equals(Object obj) {
		if (this == obj)
			return true;
		if (obj == null)
			return false;
		if (getClass() != obj.getClass())
			return false;
		ComputerDTO other = (ComputerDTO) obj;
		if (companyId == null) {
			if (other.companyId != null)
				return false;
		} else if (!companyId.equals(other.companyId))
			return false;
		if (companyName == null) {
			if (other.companyName != null)
				return false;
		} else if (!companyName.equals(other.companyName))
			return false;
		if (discontinued == null) {
			if (other.discontinued != null)
				return false;
		} else if (!discontinued.equals(other.discontinued))
			return false;
		if (id == null) {
			if (other.id != null)
				return false;
		} else if (!id.equals(other.id))
			return false;
		if (introduced == null) {
			if (other.introduced != null)
				return false;
		} else if (!introduced.equals(other.introduced))
			return false;
		if (name == null) {
			if (other.name != null)
				return false;
		} else if (!name.equals(other.name))
			return false;
		return true;
	}

	@Override
	public String toString() {
		return "ComputerDTO [id=" + id + ", name=" + name + ", introduced=" + introduced + ", discontinued="
				+ discontinued + ", companyId=" + companyId + ", companyName=" + companyName + "]";
	}	
}
